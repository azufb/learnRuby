# 範囲演算子
=begin

値の範囲を表す、範囲オブジェクトがある。
範囲を表すRangeオブジェクトの省略版。
開始値..終了値
もしくは
開始値...終了値
のように書く。

数値以外の値で範囲指定することもできる。

..
→開始値 <= 終了値

...
→開始値 < 終了値

=end

# Rangeオブジェクトを使う
p Range.new(1, 10).to_a


# 範囲演算子を使う
p (1..10).to_a

p (1...10).to_a