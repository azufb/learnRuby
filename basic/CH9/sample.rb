# 演算子

# 代入演算子
=begin

二項演算子と代入(=)を組み合わせた演算子。
例）
+=
a += 1 → a = a + 1
-=
a -= 1 → a = a - 1
*=
a *= 1 → a = a * 1
/=
a /= 1 → a = a / 1

メソッドを経由したオブジェクトの操作にも使える。

=end

